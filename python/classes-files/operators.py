'''Operadores de Asignación'''

# =: Asigna un valor
# +=: Suma y asigna
# -=: Resta y asigna
# *=: Multiplica y asigna
# /=: Divide y asigna
# //=: Divide entero y asigna
# %=: Módulo y asigna
# **=: Exponencia y asigna

'''Operadores de Comparación'''
# ==: Igual a
# !=: No igual a
# >: Mayor que
# <: Menor que
# >=: Mayor o igual que
# <=: Menor o igual que

'''Operadores Lógicos'''
# and: Verdadero si ambos operandos son verdaderos
# or: Verdadero si al menos uno de los operandos es verdadero
# not: Invierte el estado lógico de su operando

'''Operadores de Identidad'''
# is: Verdadero si ambos lados son el mismo objeto
# is not: Verdadero si ambos lados no son el mismo objeto

'''Operadores de Membresía'''
# in: Verdadero si un elemento se encuentra en una secuencia
# not in: Verdadero si un elemento no se encuentra en una secuencia
